#include "iostream"
#include "cmath"
using namespace std;

int main (int argc, char *argv[]) {
  
  cout << "Max(5, 10): " << max(5,10) << "\n";
  cout << "Min(5, 10): " << min(5,10) << "\n";
  cout << "sqrt(64): " << sqrt(64) << "\n";
  cout << "round(2.6): " << round(2.6) << "\n";
  cout << "log(2): " << log(2) << "\n";

  return 0;
}
